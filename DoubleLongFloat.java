
public class DoubleLongFloat {
	public static void main (String[] args) {
		int a = 999999;
		// long integer is called a long
		long b = 999999999999L;
		// need to add a L at the end of a long for it to work
		// what if we have a decimal??
		// use a float!
		float c = 2.5f;
		// floats are denoted at the end by an f, capital or lower case!
		// floats also have a red underline if too long, so we use doubles instead!
		double d = 2.5555555534343;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
	}

}